# Comentarios En un script R, todas las líneas que vienen precedidas por el
# símbolo # son comentarios. Es muy importante tener los scripts bien
# documentados para los posibles usuarios de los mismos y sobre todo para uno
# mismo.

# Dentro de un script, pulsar Control + Enter ejecuta la línea de código en la
# consola de R (REPL).



# ----------------------------------------
#
# R, en su forma más sencilla, es una calculadora que soporta los operadores
# aritméticos clásicos
#
# ----------------------------------------

# Suma

1 + 1

# Resta

2 - 1

# Multiplicación

2 * 3

# División

8 / 4
8 / 3

# Exponente

2^2

# Módulo (resto)

5 %% 2

# División de parte entera (desprecia el resto)

7 %/% 2

# Por supuesto, se pueden usar los paréntesis de la forma normal en que se usan
# en matemáticas, es decir, para forzar la precedencia de las operaciones

(3 - 2) / (3 + 2)
3 - 2 / 3 + 2



# ----------------------------------------
#
# También tiene operadores lógicos que devuelven TRUE (verdadero) o FALSE
# (falso)
#
# ----------------------------------------

# Igualdad
2 == 2
3 == 2

# Desigualdad
3 != 2
3 != 3

# Mayor que
3 > 2
2 > 3

# Menor que
2 < 3
3 < 2

# Mayor o igual que
3 >= 3
3 >= 4

# Menor o igual que
4 <= 4
4 <= 3
